#include "libft.h"

int	ft_is_d(int d, int i)
{
	int long	num;

	num = d;
	if (num < 0)
	{
		i = ft_is_c('-', i);
		num = num * -1;
	}
	if (num > 9)
	{
		i = ft_is_d(num / 10, i);
	}
	i = ft_is_c(num % 10 + '0', i);
	return (i);
}
